    private void limpar() {
        txtProdutor.setText("");
        txtEpsodio.setText("");
        txtnumero.setText("");
        txtDuracao.setText("");
        txtUrl.setText("");
    }

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            Podcast p = new Podcast(
                    txtProdutor.getText(),
                    txtEpsodio.getText(),
                    Integer.parseInt(txtnumero.getText()),
                    Double.parseDouble(txtDuracao.getText()),
                    txtUrl.getText()
            );
            dao.salvar(p);
            JOptionPane.showMessageDialog(this, "Podcast cadastrado com sucesso!");
            limpar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Verifique os dados Digitados");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

