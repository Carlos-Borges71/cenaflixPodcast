    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String idStr = JOptionPane.showInputDialog("Digite o ID para excluir:");
        if (idStr != null) {
            try {
                int id = Integer.parseInt(idStr);
                boolean sucesso = dao.excluir(id);

                if (sucesso) {
                    JOptionPane.showMessageDialog(this, "Exclusão concluída com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Nenhum registro encontrado com o ID informado.");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Por favor, digite um número válido para o ID.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao excluir: ");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

